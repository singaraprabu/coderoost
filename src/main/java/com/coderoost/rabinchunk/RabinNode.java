package com.coderoost.rabinchunk;

public class RabinNode {
	private int value;
	private RabinNode next;
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	public RabinNode getNext() {
		return next;
	}
	public void setNext(RabinNode next) {
		this.next = next;
	}
}
